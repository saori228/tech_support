<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;

/**
 * Контроллер главной страницы
 * Простой контроллер для отображения домашней страницы приложения
 * Проверяет авторизацию пользователя перед показом
 */
class HomeController extends Controller
{
    /**
     * Отображение главной страницы приложения
     * Проверяет, авторизован ли пользователь, и показывает соответствующую страницу
     */
    public function index()
    {
        // ПРОВЕРКА АВТОРИЗАЦИИ
        // Проверяем, авторизован ли пользователь в системе
        if (!Auth::check()) {
            // Если пользователь НЕ авторизован - перенаправляем на страницу входа
            return redirect()->route('login');
        }
        
        // ОТОБРАЖЕНИЕ ГЛАВНОЙ СТРАНИЦЫ
        // Если пользователь авторизован - показываем главную страницу
        // Представление 'home' будет содержать основной контент приложения
        return view('home');
    }
}